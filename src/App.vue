<template>
  <vn-view>
    <hero />
    <vn-view id="counter" :styleSheet="counterStyle">
      <vn-text>Counter: {{ count }}</vn-text>
      <vn-button @clicked="inc">Inc</vn-button>
    </vn-view>
  </vn-view>
</template>

<script lang="ts">
import Hero from "../src/Hero.vue";
import { ref } from "@nodegui/vue-nodegui";

export default {
  components: { Hero },
  setup() {
    const count = ref(0);
    const inc = () => {
      count.value++;
    };

    return {
      inc,
      count,
      counterStyle: `
        #counter {
          flex-direction: row;
          align-items: 'center';
          justify-content: 'center';
        }
      `,
    };
  },
};
</script>
